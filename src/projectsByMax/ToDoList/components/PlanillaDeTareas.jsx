import { useState, useEffect } from "react";
import {v4 as uuidv4 } from 'uuid';

//Planilla de tareas es un componente padre.
//Lista las tareas y actualiza el estado.
const PlanillaDeTareas = ({ loQueEnvio }) => {

  //useState actualizacion de tareas
  const [listaDeTareas, setListaDeTareas] = useState([]);

  useEffect(() => {
    // Esta función se ejecutará cuando loQueEnvio cambie
    if (loQueEnvio) {
      const nuevaTarea = {
        id: uuidv4(), //ID autogenerated,
        title: loQueEnvio,
        editable: false,
      };
      // Usamos la función de actualización para asegurarnos de que el estado se actualice correctamente.
      setListaDeTareas((prevTareas) => [...prevTareas, nuevaTarea]);
    }
  }, [loQueEnvio]);
  
  const handleEdit = (taskId) => {
    // Cambiar el estado de editable a true para habilitar la edición de la tarea
    setListaDeTareas((prevTareas) =>
      prevTareas.map((tarea) =>
        tarea.id === taskId ? { ...tarea, editable:true} : tarea
      )
    );
  };

  const handleGuardarEdit = (taskId) => {
    // Cambiar el estado de editable a true para habilitar la edición de la tarea
    setListaDeTareas((prevTareas) =>
      prevTareas.map((tarea) =>
        tarea.id === taskId ? { ...tarea, editable: false } : tarea
      )
    );
  };

  const handleBorrar = (taskId) => {
    // Cborra la tarea de la lista de tareas
    setListaDeTareas((prevTareas) =>
      prevTareas.filter((tarea) =>
        tarea.id !== taskId)
    );
  };

  const handleCompletarTarea = (taskId) => {
  //Cambia el Estado de Completo/incompleto de una tarea
  setListaDeTareas((listaDeTareas) => listaDeTareas.map((tarea) =>
    tarea.id === taskId ? {...tarea, completed:!tarea.completed} : tarea));
  }

  return (
    <div>
      {listaDeTareas.length > 0 ? (
        listaDeTareas.map((tarea) => (
          <div key={tarea.id}>
            {tarea.editable ? (
              <input
                type="text"
                value={tarea.title}
                onChange={(e) => {
                  setListaDeTareas((prevTareas) =>
                    prevTareas.map((t) =>
                      t.id === tarea.id ? { ...t, title: e.target.value } : t
                    )
                  );
                }}
              />
            ) : (
              <span><b>{tarea.title}</b>
                    <span style={{color: 'blue'}}><b>    ----     ----    </b></span>
                    <span>{tarea.completed ? 
                      (<span style={{color:'green'}}>completa</span>):
                      (<span style={{color:'red'}}>incompleta</span>)}
                    </span>
              </span>
            )}
            {tarea.editable ? (
              <button onClick={() => handleGuardarEdit(tarea.id)}>Guardar Edit</button>
            ) : (
              <button onClick={() => handleEdit(tarea.id)}>Editar</button>
            )}
            <button onClick={() => handleCompletarTarea(tarea.id)}>Completar Tarea</button>
            <button onClick={() => handleBorrar(tarea.id)}>Borrar Tarea</button>
          </div>
        ))
      ) : (
        <p>NO HAY TAREAS</p>
      )}
    </div>
  );
};

export default PlanillaDeTareas;
